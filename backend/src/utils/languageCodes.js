/**
 * Get language code from language name or code
 * @param {string} languageNameOrCode - Full language name (e.g., "Spanish") or code (e.g., "es")
 * @returns {string} - ISO 639-1 code (e.g., "es")
 */
const getLanguageCode = (languageNameOrCode) => {
    if (!languageNameOrCode) return 'en'; // Default to English

    // If it's already a valid language code, return it
    const validCodes = Object.values(LANGUAGE_CODES);
    if (validCodes.includes(languageNameOrCode)) {
        return languageNameOrCode;
    }

    // Direct match by name
    if (LANGUAGE_CODES[languageNameOrCode]) {
        return LANGUAGE_CODES[languageNameOrCode];
    }

    // Case-insensitive match by name
    const normalizedName = languageNameOrCode.trim();
    const match = Object.keys(LANGUAGE_CODES).find(
        key => key.toLowerCase() === normalizedName.toLowerCase()
    );

    return match ? LANGUAGE_CODES[match] : 'en'; // Default to English if not found
};

